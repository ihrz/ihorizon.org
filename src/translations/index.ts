export const getTranslation = (key: string, lang: 'fr' | 'en'): string => {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    value = value[k];
    if (value === undefined) {
      console.warn(`Translation missing for key: ${key} in language: ${lang}`);
      return `[${lang.toUpperCase()}] ${key}`;
    }
  }
  
  return value;
}; 